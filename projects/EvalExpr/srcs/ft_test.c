#include <stdio.h>
#include <stdlib.h>

int	ft_strlen(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
		i++;
	return (i);
}
int main(int argc, char **argv)
{
	printf("%d", ft_strlen("string"));
	return (0);
}
