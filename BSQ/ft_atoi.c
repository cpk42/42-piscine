int	ft_atoi(char *str)
{
	int	nbr;
	int i;

	i = 0;
	nbr = 0;
	while (str[i] != '\0')
	{
		nbr = nbr * 10 + (str[i] - '0');
		i++;
	}
	return (nbr);
}
